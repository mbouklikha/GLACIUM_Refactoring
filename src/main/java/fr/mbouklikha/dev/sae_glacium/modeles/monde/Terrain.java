    // Méthode privée factorisée
    private void construireHitboxDepuisMap() {
        hitboxBlocsSolides.clear();
        for (int y = 0; y < map.length; y++) {
            for (int x = 0; x < map[0].length; x++) {
                if (map[y][x] == 1 || map[y][x] == 2) {
                    hitboxBlocsSolides.add(new Hitbox(x * TAILLE_BLOC, y * TAILLE_BLOC, TAILLE_BLOC, TAILLE_BLOC));
                }
            }
        }
    }

    public void mettreAJourHitboxBlocsSolides() {
        construireHitboxDepuisMap();
    }

